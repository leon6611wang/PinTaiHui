package com.zhiyu.quanzhu.model.bean;

public class FriendSetting {
    private String avatar;
    private String username;
    private String truename;
    private String notename;
    private int messagesilence;
    private int messagetop;
    private int is_black;

    public String getAvatar() {
        return avatar;
    }

    public void setAvatar(String avatar) {
        this.avatar = avatar;
    }

    public String getUsername() {
        return username;
    }

    public void setUsername(String username) {
        this.username = username;
    }

    public String getTruename() {
        return truename;
    }

    public void setTruename(String truename) {
        this.truename = truename;
    }

    public String getNotename() {
        return notename;
    }

    public void setNotename(String notename) {
        this.notename = notename;
    }

    public int getMessagesilence() {
        return messagesilence;
    }

    public void setMessagesilence(int messagesilence) {
        this.messagesilence = messagesilence;
    }

    public int getMessagetop() {
        return messagetop;
    }

    public void setMessagetop(int messagetop) {
        this.messagetop = messagetop;
    }

    public int getIs_black() {
        return is_black;
    }

    public void setIs_black(int is_black) {
        this.is_black = is_black;
    }
}
